fn attachment_detail_parts(detail: &SharedString) -> Vec<String> {
    detail
        .as_ref()
        .split(" · ")
        .map(str::trim)
        .filter(|part| !part.is_empty())
        .map(ToOwned::to_owned)
        .collect()
}

fn attachment_size_label(attachment: &ImageAttachment) -> Option<SharedString> {
    let parts = attachment_detail_parts(&attachment.detail);
    match parts.as_slice() {
        [kind, size, ..] => Some(format!("{kind} · {size}").into()),
        [single] => Some(single.clone().into()),
        _ => None,
    }
}

fn document_parse_label(attachment: &ImageAttachment) -> SharedString {
    if attachment.detail.as_ref().contains("Parsing") {
        crate::tr!("chat_view.parsing")
    } else if let Some(parsed_text) = &attachment.parsed_text {
        crate::tr!(
            "chat_view.parsed_chars",
            count = parsed_text.chars().count()
        )
    } else if attachment.parse_error.is_some() {
        crate::tr!("chat_view.parse_failed")
    } else {
        crate::tr!("chat_view.ready")
    }
}

fn document_ocr_label(attachment: &ImageAttachment) -> Option<SharedString> {
    attachment_detail_parts(&attachment.detail)
        .into_iter()
        .find(|part| part.starts_with("OCR "))
        .map(Into::into)
}

