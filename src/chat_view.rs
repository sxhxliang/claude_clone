//! Chat conversation view — supports both plain text replies and structured
//! "cowork" replies with collapsible tool-call blocks and **streaming**
//! Markdown bodies (rendered via `TextView` + `TextViewState`). Mock data
//! mirrors the layout captured in `demo.html`.
