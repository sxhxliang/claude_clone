pub trait ChatViewState: Sized + 'static {
    fn messages(&self) -> &[ChatMessage];
    fn cowork_user_expanded(&self) -> &[bool];
    fn tool_expanded(&self) -> &HashMap<(usize, usize), bool>;
    fn pending(&self) -> bool;
    fn mode(&self) -> ChatMode;
    fn branch_origin(&self) -> Option<&BranchOrigin>;
    fn highlighted_artifact_target(&self) -> Option<ArtifactHighlightTarget> {
        None
    }
    fn message_scroll_anchor(&self, _ix: usize) -> Option<ScrollAnchor> {
        None
    }
    fn open_image_viewer(
        &self,
        image: ImageAttachment,
        window: &mut Window,
        cx: &mut Context<Self>,
    );
    fn reveal_file(&mut self, path: PathBuf, window: &mut Window, cx: &mut Context<Self>);
    fn copy_ai_message(&mut self, ix: usize, window: &mut Window, cx: &mut Context<Self>);
    fn branch_conversation_from_message(
        &mut self,
        ix: usize,
        window: &mut Window,
        cx: &mut Context<Self>,
    );
    fn open_branch_origin(
        &mut self,
        origin: BranchOrigin,
        window: &mut Window,
        cx: &mut Context<Self>,
    );
    fn delete_ai_message(&mut self, ix: usize, window: &mut Window, cx: &mut Context<Self>);
    fn regenerate_ai_message(&mut self, ix: usize, window: &mut Window, cx: &mut Context<Self>);
    fn copy_user_message(&mut self, ix: usize, window: &mut Window, cx: &mut Context<Self>);
    fn delete_user_message(&mut self, ix: usize, window: &mut Window, cx: &mut Context<Self>);
    fn edit_user_message(&mut self, ix: usize, window: &mut Window, cx: &mut Context<Self>);
    fn regenerate_from_user_message(
        &mut self,
        ix: usize,
        window: &mut Window,
        cx: &mut Context<Self>,
    );
    fn toggle_cowork_user_expanded(&mut self, ix: usize, cx: &mut Context<Self>);
    fn toggle_tool_expanded(&mut self, key: (usize, usize), cx: &mut Context<Self>);
    fn toggle_thinking_expanded(&mut self, key: (usize, usize), cx: &mut Context<Self>);
    fn open_thinking_viewer(
        &self,
        title: SharedString,
        state: Entity<TextViewState>,
        window: &mut Window,
        cx: &mut Context<Self>,
    );
}

/// Rich content block that an AI message can contain.
#[derive(Clone)]
pub enum MessageBlock {
    /// Streaming reasoning/thinking content rendered above the assistant answer.
    Thinking(ThinkingBlock),
    /// Streaming Markdown content rendered via `TextView`.
    Markdown(Entity<TextViewState>),
    /// Image generated by an image model or mock image-generation backend.
    GeneratedImage(GeneratedImage),
    /// Collapsible tool-invocation block.
    Tool(ToolCall),
}

#[derive(Clone, Copy, Debug, PartialEq, Eq)]
pub(crate) struct ArtifactHighlightTarget {
    pub(crate) message_ix: usize,
    pub(crate) kind: ArtifactHighlightKind,
}

#[derive(Clone, Copy, Debug, PartialEq, Eq)]
pub(crate) enum ArtifactHighlightKind {
    Attachment { attachment_ix: usize },
    GeneratedImage { block_ix: usize },
    ToolFile { tool_ix: usize, step_ix: usize },
}

#[derive(Clone)]
pub struct ThinkingBlock {
    pub state: Entity<TextViewState>,
    pub done: bool,
    pub expanded: bool,
}

#[derive(Clone)]
pub struct ImageAttachment {
    pub title: SharedString,
    pub url: SharedString,
    pub detail: SharedString,
    pub path: Option<PathBuf>,
    pub parsed_text: Option<SharedString>,
    pub parse_error: Option<SharedString>,
}

impl ImageAttachment {
    pub(crate) fn is_document(&self) -> bool {
        self.parsed_text.is_some()
            || self.parse_error.is_some()
            || self
                .path
                .as_deref()
                .is_some_and(crate::document_parser::is_supported_document_path)
    }
}

#[derive(Clone)]
pub struct GeneratedImage {
    pub title: SharedString,
    pub prompt: SharedString,
    pub url: SharedString,
    pub size: SharedString,
}

#[derive(Clone)]
pub struct ToolCall {
    pub title: SharedString,
    pub steps: Vec<ToolStep>,
}

#[derive(Clone)]
pub struct ToolStep {
    pub icon: IconName,
    pub title: SharedString,
    pub detail: Option<SharedString>,
    /// Optional inline "file pill" rendered under the step title.
    pub file_chip: Option<SharedString>,
    /// Final "Done" step is rendered in a brighter accent color.
    pub done: bool,
}

/// Render the chat content area.
pub fn render<T: ChatViewState>(app: &T, cx: &mut Context<T>) -> impl IntoElement {
    let messages = app.messages().to_vec();
    let user_expanded = app.cowork_user_expanded().to_vec();
    let tool_expanded = app.tool_expanded().clone();
    let pending = app.pending();
    let branch_origin = app.branch_origin().cloned();
    let branch_insert_ix = branch_origin
        .as_ref()
        .map(|origin| origin.message_count.min(messages.len()));
    let has_streaming_message = pending
        && messages.last().is_some_and(|message| {
            message.role == ChatRole::Ai
                && (message
                    .blocks
                    .as_ref()
                    .is_some_and(|blocks| !blocks.is_empty())
                    || !message.content.is_empty()
                    || !message.thinking.is_empty())
        });
    let pending_mode = messages
        .iter()
        .rev()
        .find(|message| message.role == ChatRole::User)
        .map(|message| message.mode)
        .unwrap_or(app.mode());

    v_flex()
        .w_full()
        .max_w(px(760.))
        .px_5()
        .pt_4()
        .pb_4()
        .gap_6()
        .when(branch_insert_ix == Some(0), |this| {
            this.children(
                branch_origin
                    .clone()
                    .map(|origin| render_branch_origin_timeline(origin, cx)),
            )
        })
        .children(messages.into_iter().enumerate().flat_map(|(ix, m)| {
            let mut elements = Vec::with_capacity(2);
            let highlighted_artifact = app.highlighted_artifact_target();
            let scroll_anchor = app.message_scroll_anchor(ix);
            elements.push(match m.role {
                ChatRole::User => render_user_message(
                    ix,
                    &m,
                    user_expanded.get(ix).copied().unwrap_or(false),
                    highlighted_artifact,
                    scroll_anchor,
                    cx,
                )
                .into_any_element(),
                ChatRole::Ai => render_ai_message(
                    ix,
                    &m,
                    &tool_expanded,
                    highlighted_artifact,
                    scroll_anchor,
                    cx,
                )
                .into_any_element(),
            });

            if branch_insert_ix == Some(ix + 1) {
                if let Some(origin) = branch_origin.clone() {
                    elements.push(render_branch_origin_timeline(origin, cx).into_any_element());
                }
            }
            elements
        }))
        .when(pending && !has_streaming_message, |this| {
            this.child(render_thinking(pending_mode))
        })
}

fn render_branch_origin_timeline<T: ChatViewState>(
    origin: BranchOrigin,
    cx: &mut Context<T>,
) -> impl IntoElement {
    let title = if origin.source_title.as_ref().trim().is_empty() {
        crate::tr!("conversation.untitled")
    } else {
        origin.source_title.clone()
    };
    let origin_for_click = origin.clone();

    h_flex()
        .id(format!(
            "branch-origin-{}-{}",
            origin.source_conversation_id, origin.message_count
        ))
        .w_full()
        .items_center()
        .gap_2()
        .text_size(px(12.))
        .text_color(text_3())
        .child(div().h(px(1.)).flex_1().bg(timeline_line()))
        .child(
            h_flex()
                .items_center()
                .gap_1()
                .min_w_0()
                .child(crate::tr!("chat_view.branch_from"))
                .child(
                    div()
                        .id(format!(
                            "branch-origin-title-{}-{}",
                            origin.source_conversation_id, origin.message_count
                        ))
                        .max_w(px(320.))
                        .truncate()
                        .cursor_pointer()
                        .text_color(accent())
                        .hover(|this| this.text_color(text_color()))
                        .tooltip(|window, cx| {
                            Tooltip::new(crate::tr!("chat_view.open_source")).build(window, cx)
                        })
                        .child(title)
                        .on_click(cx.listener(move |this, _, window, cx| {
                            cx.stop_propagation();
                            this.open_branch_origin(origin_for_click.clone(), window, cx);
                        })),
                ),
        )
        .child(div().h(px(1.)).flex_1().bg(timeline_line()))
}

fn render_user_message<T: ChatViewState>(
    ix: usize,
    msg: &ChatMessage,
    expanded: bool,
    highlighted_artifact: Option<ArtifactHighlightTarget>,
    scroll_anchor: Option<ScrollAnchor>,
    cx: &mut Context<T>,
) -> impl IntoElement {
    let content = msg.content.clone();
    let long = content.chars().count() > 320;
    let bubble_bg = user_bubble_bg();
    let group_id = format!("user-message-{ix}");

    v_flex()
        .id(("chat-message", ix))
        .anchor_scroll(scroll_anchor)
        .group(group_id.clone())
        .items_end()
        .child(
            v_flex()
                .max_w(px(640.))
                .bg(bubble_bg)
                .text_color(text_color())
                .rounded_xl()
                .px_4()
                .py_3()
                .gap_2()
                .when(!msg.attachments.is_empty(), |this| {
                    this.child(render_user_attachments(
                        ix,
                        &msg.attachments,
                        highlighted_artifact,
                        cx,
                    ))
                })
                .when(!content.is_empty(), |this| {
                    this.child(
                        div()
                            .text_size(px(14.))
                            .line_height(relative(1.55))
                            .child(
                                TextView::markdown(format!("user-message-content-{ix}"), content)
                                    .selectable(true),
                            )
                            .when(long && !expanded, |this| {
                                this.h(px(170.)).overflow_hidden().relative().child(
                                    div()
                                        .absolute()
                                        .left_0()
                                        .right_0()
                                        .bottom_0()
                                        .h(px(24.))
                                        .bg(linear_gradient(
                                            180.,
                                            linear_color_stop(bubble_bg.opacity(0.0), 0.),
                                            linear_color_stop(bubble_bg.opacity(0.92), 1.),
                                        )),
                                )
                            }),
                    )
                })
                .when(long, |this| {
                    this.child(
                        div()
                            .id(("show-more", ix))
                            .text_size(px(12.))
                            .text_color(text_3())
                            .cursor_pointer()
                            .hover(|h| h.text_color(text_color()))
                            .child(if expanded {
                                crate::tr!("chat_view.show_less")
                            } else {
                                crate::tr!("chat_view.show_more")
                            })
                            .on_click(cx.listener(move |this, _, _, cx| {
                                this.toggle_cowork_user_expanded(ix, cx);
                            })),
                    )
                }),
        )
        .child(render_user_actions(ix, group_id, cx))
}

fn render_user_attachments<T: ChatViewState>(
    msg_ix: usize,
    attachments: &[ImageAttachment],
    highlighted_artifact: Option<ArtifactHighlightTarget>,
    cx: &mut Context<T>,
) -> Div {
    let mut row = h_flex().gap_2().flex_wrap();
    for (ix, attachment) in attachments.iter().enumerate() {
        let highlighted = highlighted_artifact.is_some_and(|target| {
            target.message_ix == msg_ix
                && target.kind == ArtifactHighlightKind::Attachment { attachment_ix: ix }
        });
        row = row.child(render_attachment_thumbnail(
            format!("user-image-{msg_ix}-{ix}"),
            attachment,
            px(142.),
            px(96.),
            highlighted,
            cx,
        ));
    }
    row
}

fn render_ai_message<T: ChatViewState>(
    ix: usize,
    msg: &ChatMessage,
    tool_expanded: &HashMap<(usize, usize), bool>,
    highlighted_artifact: Option<ArtifactHighlightTarget>,
    scroll_anchor: Option<ScrollAnchor>,
    cx: &mut Context<T>,
) -> impl IntoElement {
    let mode = msg.mode;
    let model = msg.model.clone();
    let token_stats = (mode == ChatMode::Chat)
        .then_some(msg.token_stats)
        .flatten();
    let header = h_flex()
        .gap_2()
        .items_center()
        .mb_2()
        .child(div().text_color(accent()).text_size(px(17.)).child("✳"))
        .child(
            h_flex()
                .gap_1p5()
                .items_center()
                .min_w_0()
                .child(
                    div()
                        .text_size(px(12.))
                        .text_color(text_3())
                        .truncate()
                        .child(crate::tr!(
                            "chat_view.message_header",
                            mode = crate::i18n::chat_mode_label(mode).to_string(),
                            model = model.to_string()
                        )),
                )
                .when_some(token_stats, |this, stats| {
                    this.child(metadata_chip(token_speed_label(stats), text_3()))
                }),
        );

    let body = if let Some(blocks) = &msg.blocks {
        render_blocks(ix, blocks, tool_expanded, highlighted_artifact, cx)
    } else {
        v_flex().gap_3().child(
            div()
                .max_w(px(680.))
                .px_3p5()
                .py_2p5()
                .rounded_xl()
                .rounded_bl(px(4.))
                .bg(white_color())
                .border_1()
                .border_color(border_color())
                .shadow_sm()
                .text_size(px(14.))
                .line_height(relative(1.55))
                .child(msg.content.clone()),
        )
    };
    let group_id = format!("ai-message-{ix}");

    div()
        .id(("chat-message", ix))
        .anchor_scroll(scroll_anchor)
        .child(
            v_flex()
                .group(group_id.clone())
                .items_start()
                .child(header)
                .child(body)
                .child(render_ai_actions(ix, group_id, cx)),
        )
}

fn token_speed_label(stats: TokenUsageStats) -> SharedString {
    let speed = if stats.tokens_per_second >= 100.0 {
        format!("{:.0}", stats.tokens_per_second)
    } else {
        format!("{:.1}", stats.tokens_per_second)
    };
    crate::tr!(
        "chat_view.token_speed",
        tokens = stats.output_tokens,
        speed = speed
    )
}

