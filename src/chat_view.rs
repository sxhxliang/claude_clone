pub(crate) fn render_image_content(url: SharedString, title: SharedString) -> AnyElement {
    render_image_content_fit(url, title, ObjectFit::Cover)
}

fn render_image_content_fit(
    url: SharedString,
    title: SharedString,
    object_fit: ObjectFit,
) -> AnyElement {
    if url.starts_with("mock://") {
        return mock_image_preview(url, title);
    }

    img(url.clone())
        .size_full()
        .object_fit(object_fit)
        .with_loading({
            let url = url.clone();
            let title = title.clone();
            move || mock_image_preview(url.clone(), title.clone())
        })
        .with_fallback({
            let url = url.clone();
            let title = title.clone();
            move || mock_image_preview(url.clone(), title.clone())
        })
        .into_any_element()
}

