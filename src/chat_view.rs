fn image_seed(text: &str) -> u64 {
    text.bytes().fold(0xcbf29ce484222325u64, |hash, byte| {
        hash.wrapping_mul(0x100000001b3) ^ u64::from(byte)
    })
}

fn render_tool_call<T: ChatViewState>(
    msg_ix: usize,
    tool_ix: usize,
    tool: &ToolCall,
    expanded: bool,
    highlighted_artifact: Option<ArtifactHighlightTarget>,
    cx: &mut Context<T>,
) -> Div {
    let header_id: SharedString = format!("tool-hdr-{}-{}", msg_ix, tool_ix).into();
    // `gpui::percentage()` requires 0..=1. Collapsed = 270° clockwise = 0.75.
    let chevron_rotation: f32 = if expanded { 0.0 } else { 0.75 };
    let header = h_flex()
        .id(header_id)
        .gap_1p5()
        .items_center()
        .py_1()
        .cursor_pointer()
        .text_size(px(13.5))
        .text_color(text_2())
        .hover(|h| h.text_color(text_color()))
        .child(div().child(tool.title.clone()))
        .child(
            Icon::new(IconName::ChevronDown)
                .size_3p5()
                .rotate(gpui::percentage(chevron_rotation)),
        )
        .on_click(cx.listener(move |this, _, _, cx| {
            let key = (msg_ix, tool_ix);
            this.toggle_tool_expanded(key, cx);
        }));

    let last_ix = tool.steps.len().saturating_sub(1);
    let timeline = v_flex().when(expanded, |col| {
        let steps = tool
            .steps
            .iter()
            .enumerate()
            .map(|(i, step)| {
                render_tool_step(
                    step,
                    msg_ix,
                    tool_ix,
                    i,
                    i == last_ix,
                    highlighted_artifact,
                    cx,
                )
            })
            .collect::<Vec<_>>();
        col.children(steps)
    });

    v_flex().w_full().child(header).child(timeline)
}

fn render_tool_step<T: ChatViewState>(
    step: &ToolStep,
    msg_ix: usize,
    tool_ix: usize,
    step_ix: usize,
    last: bool,
    highlighted_artifact: Option<ArtifactHighlightTarget>,
    cx: &mut Context<T>,
) -> Div {
    let line_color: Hsla = timeline_line();
    let chip_bg: Hsla = file_chip_bg();
    let chip_hover_bg: Hsla = file_chip_hover_bg();
    let icon_color = if step.done { accent() } else { text_3() };

    h_flex()
        .gap_0()
        .items_stretch()
        .child(
            v_flex()
                .w(px(28.))
                .items_center()
                .flex_shrink_0()
                .child(
                    div()
                        .size_5()
                        .flex()
                        .items_center()
                        .justify_center()
                        .text_color(icon_color)
                        .child(Icon::new(step.icon.clone()).size_4()),
                )
                .when(!last, |this| {
                    this.child(div().w(px(1.)).flex_1().min_h(px(12.)).bg(line_color))
                }),
        )
        .child(
            v_flex()
                .pl_2()
                .pt(px(2.))
                .pb_2()
                .gap_1p5()
                .flex_1()
                .min_w_0()
                .child(
                    div()
                        .text_size(px(13.5))
                        .text_color(if step.done { text_color() } else { text_2() })
                        .child(step.title.clone()),
                )
                .when_some(step.detail.clone(), |this, detail| {
                    this.child(
                        div()
                            .max_w(px(520.))
                            .text_size(px(12.))
                            .line_height(px(18.))
                            .text_color(text_3())
                            .child(detail),
                    )
                })
                .when_some(step.file_chip.clone(), |this, chip| {
                    let chip_label = chip.clone();
                    let chip_id = msg_ix * 1_000_000 + tool_ix * 1_000 + step_ix;
                    let highlighted = highlighted_artifact.is_some_and(|target| {
                        target.message_ix == msg_ix
                            && target.kind == ArtifactHighlightKind::ToolFile { tool_ix, step_ix }
                    });
                    this.child(
                        h_flex()
                            .id(("file-chip", chip_id))
                            .gap_1p5()
                            .items_center()
                            .px_2()
                            .py_0p5()
                            .rounded_md()
                            .border_1()
                            .border_color(if highlighted { accent() } else { chip_bg })
                            .when(highlighted, |this| this.border_2())
                            .bg(chip_bg)
                            .text_size(px(11.5))
                            .text_color(text_2())
                            .w(px(240.))
                            .cursor_pointer()
                            .hover(|h| h.bg(chip_hover_bg).text_color(text_color()))
                            .child(Icon::new(IconName::File).size_3())
                            .child(div().truncate().flex_1().child(chip))
                            .on_click(cx.listener(move |_, _, window, cx| {
                                window.push_notification(
                                    Notification::info(crate::tr!(
                                        "chat_view.opened",
                                        name = chip_label.to_string()
                                    )),
                                    cx,
                                );
                            })),
                    )
                }),
        )
}

